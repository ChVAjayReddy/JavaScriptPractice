public class SwapTwoNumbers{
    static int x=10;
    static  int y=20;
    public static void swap() {
        int temp=x;
        int newx=y;
        int newy=temp;
        System.out.println("After Swapping x :"+newx+ " y :" +newy);
        
    }
    public static void main(String[] args) {
        swap();
    }

}